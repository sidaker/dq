import math

print(2**5) # Power
print(9**2) # Power
print(math.sqrt(4)) # Square root.
print(math.sqrt(8))
# Get Cube root . N**(1.0/3.0)
print("cube root")
print(8**(1.0/3.0))

print("Fourth root")
print(round(16**(1.0/4.0)))

print(math.log(8))
print(math.log(4))

# Get answer to log 36 base 6. => 2 as 6 to the power 2 is 36.
print("log 36 base 6")
print(math.log(36,6))

# log 2. i.e base 2.
print("base 2")
print(math.log2(4))
print(math.log2(16))
print(math.log2(32))
print(math.log2(128))

# log 10. i.e base 10.
print("base 10")
print(math.log10(100))
print(math.log10(1000))
print(math.log10(32))
print(math.log10(10000))

# Natural log. base e.  the natural logarithm of 1+x (base e).
print("base e Natural log")
print(math.log1p(100))
print(math.log1p(32))


